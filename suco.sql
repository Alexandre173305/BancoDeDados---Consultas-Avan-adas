SELECT 
    TP.SABOR, 
    YEAR(NF.DATA_VENDA) AS ANO,
    SUM(INF.QUANTIDADE) AS QUANTIDADE_VENDIDA,
    ROUND(
        (SUM(INF.QUANTIDADE) / (
            SELECT SUM(INF2.QUANTIDADE) 
            FROM itens_notas_fiscais INF2 
            INNER JOIN notas_fiscais NF2 ON INF2.NUMERO = NF2.NUMERO 
            WHERE YEAR(NF2.DATA_VENDA) = 2016
        )) * 100, 2
    ) AS PARTICIPACAO_PERCENTUAL
FROM tabela_de_produtos TP
INNER JOIN itens_notas_fiscais INF ON TP.CODIGO_DO_PRODUTO = INF.CODIGO_DO_PRODUTO
INNER JOIN notas_fiscais NF ON INF.NUMERO = NF.NUMERO
WHERE YEAR(NF.DATA_VENDA) = 2016
GROUP BY TP.SABOR, YEAR(NF.DATA_VENDA)
ORDER BY QUANTIDADE_VENDIDA DESC;